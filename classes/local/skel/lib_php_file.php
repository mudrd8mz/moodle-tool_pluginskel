namespace tool_pluginskel\local\skel;

use tool_pluginskel\local\util\exception;

defined('MOODLE_INTERNAL') || die();

/**
 * Class representing the plugin's lib.php file.
 *
 * @copyright   2016 Alexandru Elisei <alexandru.elisei@gmail.com>
 * @license     http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class lib_php_file extends php_internal_file {

    /**
     * Set the data to be eventually rendered.
     *
     * @param array $data
     */
    public function set_data(array $data) {

        parent::set_data($data);

        // The plugin must advertise the fact it supports making a backup.
        if (!empty($this->data['backup'])) {
            if (empty($this->data['supported_features'])) {
                $this->data['supported_features'] = array();
            }
            $this->data['supported_features'][] = 'FEATURE_BACKUP_MOODLE2';
        }

        if (!empty($this->data['supported_features'])) {
            $this->data['has_supported_features'] = true;
        }
    }
}
